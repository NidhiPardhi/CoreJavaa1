package com.rays.basics;

public class SwitchInt {

	public static void main(String[] args) {
		 
		int num= 100;
		
		switch(num) {
		
		case 300:
			
			System.out.println("num3 = " + num);
			break;
			
		case 200:
			
			System.out.println("num2 = " + num);
			break;
			
		case 100:
			
			System.out.println("num1 = " + num);
			break;
			
		default:
			
			System.out.println("non of the above");
		}
	}
}
