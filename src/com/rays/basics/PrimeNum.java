package com.rays.basics;

public class PrimeNum {

	public static void main(String[] args) {
		
		int x=7;
	
		
		for(int i=2;i<=100;i++) {
		
			
		if( x % i == 0 )	{
			
		
    			
	     System.out.println("x is not a prime no "+x);
	     break;
		}
		else {
			
			System.out.println("x is a prime no "+x);
			break;
		}
		
			
	    
			
			
		}
	}
} 